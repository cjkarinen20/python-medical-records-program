class Patient:
    
    def __init__(self, first_name, last_name, date_of_birth, height, weight, taking_meds):
        self.first_name = first_name
        self.last_name = last_name
        self.date_of_birth = date_of_birth
        self.height = height
        self.weight = weight
        self.taking_meds = taking_meds